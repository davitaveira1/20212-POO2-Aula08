    private void btAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAcessarActionPerformed
        // TODO add your handling code here:

        String login = txtCampoLogin.getText();
        String senha = txtCampoSenha.getText();

        
        
        if (controleUser.verificarUserSenha(login, senha)) {

            TelaPrincipal tp = new TelaPrincipal();
            tp.lbUserLogado.setText(login);            
            tp.setVisible(true);
            dispose();
            
        } else {
            JOptionPane.showMessageDialog(null, "User/Senha inv√°lidos!");
            txtCampoLogin.setText("");
            txtCampoSenha.setText("");
            txtCampoLogin.requestFocus();
        }

    }//GEN-LAST:event_btAcessarActionPerformed

